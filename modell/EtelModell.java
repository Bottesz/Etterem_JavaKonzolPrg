
package modell;


public class EtelModell {
    private String nev;
    private int ar;

    public EtelModell(String nev, int ar) {
        this.nev = nev;
        this.ar = ar;
    }

    public String getNev() {
        return nev;
    }

    public int getAr() {
        return ar;
    }
    
    
    
}
